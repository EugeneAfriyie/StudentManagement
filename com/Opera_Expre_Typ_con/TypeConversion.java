package com.Opera_Expre_Typ_con;

public class TypeConversion {
     public static void main(String[] args) {
        double salary = 2500.75;
        int salaryInt = (int) salary;  
        System.out.println("Salary as int: " + salaryInt);

        int totalMarks = 480;
        float totalMarksFloat = (float) totalMarks;  
        System.out.println("Total Marks as float: " + totalMarksFloat);
    }
}
